package com.moment.admin.model;

public class AdminDTO {

    private int adminNo;
    private String adminId;
    private String adminPwd;
    private String adminName;
    private String adminPhone;
    private String adminAddr;
    private String adminBirth;
    private String adminGrade;
    private String adminTeam;

    public int getAdminNo( ) {
        return adminNo;
    }

    public void setAdminNo(int adminNo) {
        this.adminNo = adminNo;
    }

    public String getAdminId( ) {
        return adminId;
    }

    public void setAdminId(String adminId) {
        this.adminId = adminId;
    }

    public String getAdminPwd( ) {
        return adminPwd;
    }

    public void setAdminPwd(String adminPwd) {
        this.adminPwd = adminPwd;
    }

    public String getAdminName( ) {
        return adminName;
    }

    public void setAdminName(String adminName) {
        this.adminName = adminName;
    }

    public String getAdminPhone( ) {
        return adminPhone;
    }

    public void setAdminPhone(String adminPhone) {
        this.adminPhone = adminPhone;
    }

    public String getAdminAddr( ) {
        return adminAddr;
    }

    public void setAdminAddr(String adminAddr) {
        this.adminAddr = adminAddr;
    }

    public String getAdminBirth( ) {
        return adminBirth;
    }

    public void setAdminBirth(String adminBirth) {
        this.adminBirth = adminBirth;
    }

    public String getAdminGrade( ) {
        return adminGrade;
    }

    public void setAdminGrade(String adminGrade) {
        this.adminGrade = adminGrade;
    }

    public String getAdminTeam( ) {
        return adminTeam;
    }

    public void setAdminTeam(String adminTeam) {
        this.adminTeam = adminTeam;
    }
}
